#include <stdio.h>

int main() {
    int a = 178;
    int b = 104;
    int c = 10;
    // 1번을 해보세요!
    printf("%d & %d = %d\n", a, b, a & b);
    // 2번을 해보세요!
    printf("%d | %d = %d\n", a, b,a | b);
    // 3번을 해보세요!
    printf("%d ^ %d = %d\n", a, b,a ^ b);
    // 4번을 해보세요!
    printf("c = %d, ~c = %d\n", c, ~c);

    return 0;
}


