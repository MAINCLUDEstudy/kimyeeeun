#include <stdio.h>

int main() {
    int a = 10, b = 2;
    // 1번을 해보세요!
    printf("a 더하기 b = %d\n", a + b);
    // 2번을 해보세요!
    printf("a 빼기   b = %d\n", a - b);
    // 3번을 해보세요!
    printf("a 곱하기 b = %d\n", a * b);
    // 4번을 해보세요!
    printf("a 나누기 b = %d\n", a / b);
    // 5번을 해보세요!
    printf("a 나머지 b = %d\n", a % b);

    return 0;
}
